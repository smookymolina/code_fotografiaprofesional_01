import nodemailer from 'nodemailer'

const transporter = nodemailer.createTransport({
  host: process.env.SMTP_HOST,
  port: Number(process.env.SMTP_PORT) || 587,
  secure: false,
  auth: {
    user: process.env.SMTP_USER,
    pass: process.env.SMTP_PASS,
  },
})

export async function sendContactNotification(data: {
  name: string
  email: string
  phone?: string
  eventDate?: string
  service: string
  message: string
}): Promise<void> {
  if (!process.env.SMTP_USER) return // Email no configurado, saltar silenciosamente

  await transporter.sendMail({
    from: process.env.SMTP_FROM,
    to: process.env.SMTP_USER,
    subject: `Nueva solicitud de contacto - ${data.service}`,
    html: `
      <h2>Nueva solicitud de Studio Lumière</h2>
      <p><strong>Nombre:</strong> ${data.name}</p>
      <p><strong>Email:</strong> ${data.email}</p>
      <p><strong>Teléfono:</strong> ${data.phone || 'No proporcionado'}</p>
      <p><strong>Fecha tentativa:</strong> ${data.eventDate || 'No especificada'}</p>
      <p><strong>Servicio:</strong> ${data.service}</p>
      <p><strong>Mensaje:</strong></p>
      <p>${data.message}</p>
    `,
  })
}

export async function sendWelcomeEmail(data: {
  to: string
  name: string
  tempPassword?: string
}): Promise<void> {
  if (!process.env.SMTP_USER) return

  await transporter.sendMail({
    from: process.env.SMTP_FROM,
    to: data.to,
    subject: 'Bienvenido a Studio Lumière',
    html: `
      <h2>¡Bienvenido, ${data.name}!</h2>
      <p>Tu cuenta ha sido creada exitosamente en Studio Lumière.</p>
      ${data.tempPassword ? `<p>Contraseña temporal: <strong>${data.tempPassword}</strong></p>` : ''}
      <p>Inicia sesión en: <a href="${process.env.FRONTEND_URL}/login">Studio Lumière</a></p>
    `,
  })
}

export async function sendBookingConfirmation(data: {
  to: string
  name: string
  service: string
  eventDate: string
  bookingId: string
}): Promise<void> {
  if (!process.env.SMTP_USER) return

  await transporter.sendMail({
    from: process.env.SMTP_FROM,
    to: data.to,
    subject: 'Confirmación de reserva - Studio Lumière',
    html: `
      <h2>¡Tu reserva fue recibida, ${data.name}!</h2>
      <p><strong>Servicio:</strong> ${data.service}</p>
      <p><strong>Fecha del evento:</strong> ${data.eventDate}</p>
      <p><strong>ID de reserva:</strong> ${data.bookingId}</p>
      <p>Nos pondremos en contacto contigo para confirmar los detalles.</p>
    `,
  })
}
